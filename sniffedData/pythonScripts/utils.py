# revert bytes

data = """
0xB4; 0x6D; 0x00; 0xFF; 0xFF; 0xFF; 0xFF; 0xBC
0xB4; 0x6C; 0x00; 0xFF; 0xFF; 0xFF; 0xFF; 0xBC
0xB4; 0x6B; 0x00; 0xFF; 0xFF; 0xFF; 0xFF; 0xBC
0xB4; 0x6A; 0x00; 0xFF; 0xFF; 0xFF; 0xFF; 0xBC 
0xB4; 0x69; 0x00; 0xFF; 0xFF; 0xFF; 0xFF; 0xBC
0xB4; 0x2E; 0xCC; 0xCC; 0x86; 0xB7; 0xFF; 0xBC
0xB4; 0x2E; 0xCC; 0xCC; 0x86; 0xB7; 0xFF; 0xBC
"""

lines = data.splitlines()
for line in lines:
    line = line.strip()
    if line != '':
        byteData = line.split('; ')

        byteList = []
        for byte in byteData:
            byte = byte.strip().strip(';')

            inverseNum = hex((~int(byte, 16)) & 0xFF)
            inverseNum = str(inverseNum).upper()
            inverseNum = inverseNum.replace("0X", "0x")
            byteList.append(inverseNum)

        print(': '.join(byteList))
